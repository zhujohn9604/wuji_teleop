

def print_green(x):
    return print("\033[92m {}\033[00m".format(x))

def print_red(x):
    return print("\033[91m {}\033[00m".format(x))

def print_yellow(x):
    return print("\033[93m {}\033[00m".format(x))

def print_blue(x):
    return print("\033[94m {}\033[00m".format(x))

def print_purple(x):
    return print("\033[95m {}\033[00m".format(x))

def print_cyan(x):
    return print("\033[96m {}\033[00m".format(x))

def print_light_gray(x):
    return print("\033[97m {}\033[00m".format(x))

def print_black(x):
    return print("\033[98m {}\033[00m".format(x))

def print_bold(x):
    return print("\033[1m {}\033[0m".format(x))

def print_underline(x):
    return print("\033[4m {}\033[0m".format(x))